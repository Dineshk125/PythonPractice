"""Data types in python"""
# 1. String
# 2. Integer
# 3. Float
# 4. Boolean
# 5. List
# 6. Tuple
# 7. Dictionary
# 8. Set
# 9. None

#String Data type
name = "Dinesh"
age = 20
is_student = True
print(name, age, is_student)

print(type(name))
print(type(age))
print(type(is_student))

# Integer Data type
age = 20
print(age)
print(type(age))

# Float Data type
pi = 3.14
print(pi)
print(type(pi))

# Boolean Data type
is_student = True
print(is_student)
print(type(is_student))

# List Data type
fruits = ["apple", "banana", "cherry"]
print(fruits)
print(type(fruits))

# Tuple Data type
coordinates = (10, 20)
print(coordinates)
print(type(coordinates))

# Dictionary Data type
person = {"name": "Dinesh", "age": 20}
print(person)
print(type(person))

# Set Data type
fruits = {"apple", "banana", "cherry"}
print(fruits)
print(type(fruits))

# None Data type
name = None
print(name)
print(type(name))

